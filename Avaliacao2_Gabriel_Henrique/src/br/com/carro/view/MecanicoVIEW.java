    private void gravar(){
        try{
            mecanicoDTO.setNome_mec(nome_mec.getText());
            mecanicoDTO.setCpf_mec(cpf_mec.getText());
            mecanicoDTO.setTel_mec(tel_mec.getText());
            
            JOptionPane.showMessageDialog(null,
                    mecanicoCTR.inserirMecanico(mecanicoDTO)
            );
        }
        catch(Exception e){
            System.out.println("Erro ao Gravar" + e.getMessage());
        }
    }//Fecha método gravar()
    
    
