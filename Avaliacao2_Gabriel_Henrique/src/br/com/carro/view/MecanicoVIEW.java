    /**
     * Método utilizado para preencher/contruir a Jtable.
     * @param nome, String com o nome do mecanico
     */
    private void preencheTabela(String nome_mec){
        try{
            //Limpa todas as linhas
            modelo_jtl_consultar_mecanico.setNumRows(0);
            //Enquanto tiver linhas - faça
            mecanicoDTO.setNome_mec(nome_mec);
            rs = mecanicoCTR.consultarMecanico(mecanicoDTO, 1); //1 = é a pesquisa por nome na classe DAO
            while(rs.next()){
                modelo_jtl_consultar_mecanico.addRow(new Object[]{
                  rs.getString("id_mec"),
                  rs.getString("nome_mec"),
                });
            }        
        }
        catch(Exception erTab){
            System.out.println("Erro SQL: "+erTab);
        } 
        finally{
            mecanicoCTR.CloseDB();
        }
    }//Fecha método preencheTabela(String nome)
    
    /**
     * Método utilizado para preencher os campos da tela com 
     * valores do mecanico.
     * @param id_mec, int com o id do mecanico.
     */
    private void preencheCampos(int id_mec){
        try{
            mecanicoDTO.setId_mec(id_mec);
            rs = mecanicoCTR.consultarMecanico(mecanicoDTO, 2); //2 = é a pesquisa no id na classe DAO
            if(rs.next()){
                limpaCampos();
                
                nome_mec.setText(rs.getString("nome_mec"));
                tel_mec.setText(rs.getString("tel_mec"));
                cpf_mec.setText(rs.getString("cpf_mec"));
                
                gravar_alterar = 2;
                liberaCampos(true);
            }//fecha if(rs.next)
        }//fecha try
        catch(Exception erTab){
            System.out.println("Erro SQL: "+erTab);
        }  
        finally{
            mecanicoCTR.CloseDB();
        }
    }//Fecha método preencheCampos(int id_mecanico)
    
    
    

