    /**
     * Método utilizado para gravar os dados do carro.
     */
    private void gravar(){
        try{           
            carroDTO.setModelo_car(modelo_car.getText());
            carroDTO.setAno_car(ano_car.getText());
            carroDTO.setMarca_car(marca_car.getText());
            donoDTO.setId_don(Integer.parseInt(String.valueOf(
                    jtl_Dono.getValueAt(
                    jtl_Dono.getSelectedRow(), 0))));

            JOptionPane.showMessageDialog(null,
                    carroCTR.inserirCarro(carroDTO, donoDTO)
            );
        }
        catch(Exception e){
            System.out.println("Erro ao Gravar" + e.getMessage());
        }
    }//Fecha método gravar()
    
    
    /**
     * Método utilizado para alterar os dados do carro.
     */
    private void alterar(){
        try{
            carroDTO.setModelo_car(modelo_car.getText());
            carroDTO.setAno_car(ano_car.getText());
            carroDTO.setMarca_car(marca_car.getText());
            donoDTO.setId_don(Integer.parseInt(String.valueOf(
                    jtl_Dono.getValueAt(
                    jtl_Dono.getSelectedRow(), 0))));
            
            JOptionPane.showMessageDialog(null,
                    carroCTR.alterarCarro(carroDTO, donoDTO)
            );
        }
        catch(Exception e){
            System.out.println("Erro ao Alterar" + e.getMessage());
        }
    }//Fecha método alterar()
    
    
    /**
     * Método utilizado para excluir os dados do carro.
     */
    private void excluir(){
       if(JOptionPane.showConfirmDialog(null, "Deseja Realmente excluir o Carro?","Aviso", 
            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null,
                    carroCTR.excluirCarro(carroDTO)
            );
       }
    }//Fecha método excluir()
    
    
    
