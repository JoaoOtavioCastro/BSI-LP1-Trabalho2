    /**
     * Método utilizado para preencher/contruir a Jtable.
     * @param nome_car, String com o nome do carro
     */
    private void preencheTabelaCarro(String nome_car){
        try{
            //Limpa todas as linhas
            modelo_jtl_consultar_carro.setNumRows(0);
            //Enquanto tiver linhas - faça
            carroDTO.setModelo_car(nome_car);
            rs = carroCTR.consultarCarro(carroDTO, 1); //1 = é a pesquisa por nome na classe DAO
            while(rs.next()){
                modelo_jtl_consultar_carro.addRow(new Object[]{
                  rs.getString("id_car"),
                  rs.getString("modelo_car"),
                });
            }        
        }
        catch(Exception erTab){
            System.out.println("Erro SQL: "+erTab);
        } 
        finally{
            carroCTR.CloseDB();
        }
    }//Fecha método preencheTabelaCarro(String nome_car)
    
    /**
     * Método utilizado para preencher os campos da tela com 
     * valores do carro.
     * @param id_car, int com o id do carro.
     */
    private void preencheCamposCarro(int id_car){
        try{
            carroDTO.setId_car(id_car);
            rs = carroCTR.consultarCarro(carroDTO, 2); //2 = é a pesquisa no id na classe DAO
            if(rs.next()){
                limpaCampos();
                
                modelo_car.setText(rs.getString("modelo_car"));
                ano_car.setText(rs.getString("ano_car"));
                marca_car.setText(rs.getString("marca_car"));
               
                /////Colocando os dados do dono
                modelo_jtl_consultar_fono.setNumRows(0);
                modelo_jtl_consultar_fono.addRow(new Object[]{rs.getInt("id_don"), rs.getString("nome_don"),});
                jtl_Dono.setRowSelectionInterval(0, 0);
                
                gravar_alterar = 2;
                liberaCampos(true);
            }//fecha if(rs.next)
        }//fecha try
        catch(Exception erTab){
            System.out.println("Erro SQL: "+erTab);
        }  
        finally{
            carroCTR.CloseDB();
        }
    }//Fecha método preencheCamposCarro(int id_don)
    
    
